//* ¿Qué es Git?
// Git es un sistema de control de versiones que te permite guardar, rastrear y revertir cambios en tu código.

//*¿Para qué sirve?
// Lleva el historial de tu proyecto.
// Te permite colaborar con otros sin sobrescribir el trabajo.
// Puedes volver a versiones anteriores si algo falla.

//* ¿Por qué debes tenerlo en tu terminal?
// Porque con comandos como git init, git add, git commit, y git push, puedes gestionar tu proyecto, subirlo a GitHub y trabajar de forma profesional como lo hacen en empresas reales.

// 👉 Es una herramienta esencial si estás aprendiendo desarrollo.

//? ¿Qué es GitHub?
// GitHub es una plataforma en la nube donde puedes almacenar tus proyectos con Git y compartirlos con el mundo. Es como un "Google Drive" para tu código, pero con funciones pensadas para desarrolladores.

//* ¿Cómo se complementa con Git?
// Git maneja los cambios en tu proyecto localmente.
// GitHub te permite subir esos cambios a Internet, trabajar desde diferentes computadoras o con otras personas.
//* 🔁 Git + GitHub trabajan juntos así:

// Git guarda los cambios en tu computadora (git commit).
// GitHub recibe esos cambios cuando haces git push.
// Y desde GitHub puedes colaborar con tu equipo de trabajo, otras empresas u otros programadores o desarrolladores, revisar código, hacer colaboraciones o mostrar tu portafolio y que tanto produces diariamente, semanalmente o mensualmente, lo ideal es hacerlo a diario como hábito de lunes a sábado y domingo descansas de estudiar, porque es imoprtante descansar tu mente.

//* 👉 En resumen:
// Git es la herramienta, GitHub es el lugar donde compartes lo que haces con Git.